func MsgTxFromHex(hexTx string) (*wire.MsgTx, error) {
	tx := new(wire.MsgTx)
	err := tx.Deserialize(hex.NewDecoder(strings.NewReader(hexTx)))
	if err != nil {
		return nil, err
	}

	return tx, nil
}

func TestIsMixedSplitTx(t *testing.T) {
	ticketPrice0 := int64(13909274457)
	tx0, err := MsgTxFromHex(hugeMixedSplitTx)
	if err != nil {
		t.Fatal(err)
	}

	ticketPrice1 := int64(12978960619)
	tx1, err := MsgTxFromHex(smallNonMixedPoolSplitTx)
	if err != nil {
		t.Fatal(err)
	}

	coinbaseTx, err := MsgTxFromHex(coinbaseTxHex)
	if err != nil {
		t.Fatal(err)
	}

	tests := []struct {
		name           string
		tx             *wire.MsgTx
		ticketPrice    int64
		isMix          bool
		wantNumTickets uint32
		wantMixDenom   int64
	}{
		{
			"ok",
			tx0,
			ticketPrice0,
			true,
			9,
			13909277437,
		},
		{
			"ok",
			tx1,
			ticketPrice1,
			false,
			0,
			0,
		},
		{
			"no, coinbase",
			coinbaseTx,
			ticketPrice0,
			false,
			0,
			0,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			isMix, mixDenom, numTickets := IsMixedSplitTx(tt.tx, int64(txrules.DefaultRelayFeePerKb), tt.ticketPrice)
			if isMix != tt.isMix {
				t.Errorf("IsMixedSplitTx() isMix = %v, wanted %v", isMix, tt.isMix)
			}
			if numTickets != tt.wantNumTickets {
				t.Errorf("IsMixedSplitTx() numTickets = %v, wanted %v", numTickets, tt.wantNumTickets)
			}
			if mixDenom != tt.wantMixDenom {
				t.Errorf("IsMixedSplitTx() mixDenom = %v, wanted %v", mixDenom, tt.wantMixDenom)
			}
		})
	}
}

func TestIsMixTx(t *testing.T) {

	// 11x 2.68435456
	tx0, err := MsgTxFromHex(mix0Hex)
	if err != nil {
		t.Fatal(err)
	}

	fmt.Printf("Aqui: %+v\n\n", mix0Hex)
	coinbaseTx, err := MsgTxFromHex(coinbaseTxHex)
	if err != nil {
		t.Fatal(err)
	}

	tests := []struct {
		name         string
		tx           *wire.MsgTx
		want         bool
		wantDenom    int64
		wantMixTotal uint32
	}{
		{
			"ok",
			tx0,
			true,
			268435456,
			11,
		},
		{
			"no, coinbase",
			coinbaseTx,
			false,
			0,
			0,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got, mixDenom, mixTotal := IsMixTx(tt.tx)
			if got != tt.want {
				t.Errorf("IsMixTx() got = %v, want %v", got, tt.want)
			}
			if mixDenom != tt.wantDenom {
				t.Errorf("IsMixTx() mixDenom = %v, want %v", mixDenom, tt.wantDenom)
			}
			if mixTotal != tt.wantMixTotal {
				t.Errorf("IsMixTx() mixTotal = %v, want %v", mixTotal, tt.wantMixTotal)
			}
		})
	}
}

